#[allow(unused)]

struct MathFunc {
    add: i32,
    sub: i32,
    mul: i32,
    div: i32,
}

impl MathFunc {
    pub fn _add(a: i32, b: i32) -> i32 {
        a + b
    }

    pub fn _sub(a: i32, b: i32) -> i32 {
        a - b
    }

    pub fn _mul(a: i32, b: i32) -> i32 {
        a * b
    }

    pub fn _div(a: i32, b: i32) -> i32 {
        a / b
    }
}
